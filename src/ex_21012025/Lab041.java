package ex_21012025;

public class Lab041 {
    public static void main(String[] args) {
        //compound assignment operator
        int a= 10;
        // +=, -+, /+ %=
        a=a+10;
        a+=10;// above both work same
        System.out.println(a);
        a-=10;
        System.out.println(a);
        a/=10;
        System.out.println(a);
        a%=10;
        System.out.println(a);
        a*=10;
        System.out.println(a);
    }
}
